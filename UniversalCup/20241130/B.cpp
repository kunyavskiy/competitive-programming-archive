typedef long long ll;
typedef long double ld;

void solve() {
  int n, m;
  scanf("%d%d", &n, &m);
  if (__gcd(n, m) != 1) {
    printf("No\n");
    return;
  }

  printf("Yes\n");
  for (int i = 0; i < n; i++) {
    printf("%lld%c", (i * 1LL * m + 1) % (n * 1LL * m), " \n"[i == n - 1]);
  }
  for (int i = 0; i < m; i++) {
    printf("%lld%c", (i * 1LL * n + 1) % (n * 1LL * m), " \n"[i == m - 1]);
  }
}

