class FoxAndMp3 {
vector<string> res;

string convert(ll val){
	stringstream str;
	str << val << ".mp3";
	string s;
	str >> s;
	return s;
}

void go(ll pos,ll max){
	if (res.size() >= 50) return;
	if (pos > max) return;
	if (pos) res.pb(convert(pos));
	for (int i = !pos; i < 10; i++)
		go(pos*10+i,max);
}

public:
    vector <string> playList(int n) {
        go(0LL,n);
        return res;
    }
};


