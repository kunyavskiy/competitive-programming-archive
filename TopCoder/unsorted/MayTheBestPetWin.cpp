class MayTheBestPetWin {

int _dp[2][50 * 10000 * 2 + 100];
int* dp[2];

public:
    int calc(vector <int> a, vector <int> b) {
    	int n = a.size();
        dp[0] = _dp[0] + SHIFT;
        dp[1] = _dp[1] + SHIFT;
        
        dp[0][0] = 1;
        for (int i = 0; i < n; i++){
	        for (int j = -SHIFT+10; j < SHIFT; j++)
	            if (dp[0][j]) {
	            	dp[1][j + b[i]] = 1;
	            	dp[1][j - a[i]] = 1;
	            }
	        for (int j = -SHIFT+10; j < SHIFT; j++)
	             dp[0][j] = dp[1][j], dp[1][j] = 0;	        
	    }
	    int ans = 0;
	    for (int i = 0; i < n; i++)
	    	ans += b[i] - a[i];
	    ans = (ans + 1) / 2;
	    while (!dp[0][ans])
	    	ans++;
	    return ans;
    }
};


