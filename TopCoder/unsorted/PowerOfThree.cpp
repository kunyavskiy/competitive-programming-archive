class PowerOfThree {
public:
    string ableToGet(int x, int y) {
        while (true){
            if (x == 0 && y == 0) return "Possible";
            int a = (x % 3 + 3) % 3;
            int b = (y % 3 + 3) % 3;
            if (a && b) return "Impossible";
            if (!a && !b) return "Impossible";;
            if (a == 2) x++;
            else if (a == 1) x--;
            else if (b == 2) y++;
            else if (b == 1) y--;
            else assert(false);
            assert(x % 3 == 0);
            assert(y % 3 == 0);
            x /= 3;
            y /= 3;        
        }
    }
};


