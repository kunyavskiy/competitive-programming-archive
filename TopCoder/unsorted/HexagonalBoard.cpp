class HexagonalBoard {

const int dx[6] = {-1, -1, 0, 1, 1, 0};
const int dy[6] = {0, 1, 1, 0, -1, -1};

int ans;
vector<string> v;
int n,m;

void dfs(int x,int y,int c){
	if (x < 0 || x >= n) return;
	if (y < 0 || y >= m) return;
	if (v[x][y] == '-') return;
	ans = max(ans, 1);
	if (c != -1) ans = max(ans, 2);
	if (c == -1) c = 0;
	
	if (v[x][y] == 'X'){
		v[x][y] = char(c);
		for (int i = 0; i < 6; i++)
			dfs(x + dx[i], y + dy[i], 1 - c);
		return;
	}
	if (v[x][y] != c)
		ans = 3;	
}

public:
    int minColors(vector <string> _v) {
    	v = _v;
    	n = (int)v.size();
    	m = (int)v[0].size();
    	ans = 0;
        for (int i = 0; i < n; i++)
        	for (int j = 0; j < m; j++)
        		if (v[i][j] == 'X')
        			dfs(i,j, -1);
        return ans;
    }
};


