class SetAndSet {

long long dp[2][(1<<20)+10];

public:
	long long countandset(vector <int> A) {
		int n = A.size();
		int aand = A[0];
		for (int i = 0; i < (int)A.size(); i++)
			aand &= A[i];
		for (int i = 0; i < (int)A.size(); i++)
			A[i] ^= aand;
		
		//dp[0][0] = 1;	
			
		for (int i = 0; i < n; i++){
			memset(dp[1],0,sizeof(dp[1]));
			for (int j = 0; j < (1<<20); j++)
				dp[1][j] += dp[0][j], dp[1][j&A[i]] += dp[0][j];
			dp[1][A[i]]++;
			memcpy(dp[0],dp[1],sizeof(dp[0])); 
		}
		
		return (dp[0][0]-1)/2;
	}
};


