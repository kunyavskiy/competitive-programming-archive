class KleofasTail {
public:
	long long countGoodSequences(long long K, long long A, long long B) {
		if (K == 0)
			return B - A + 1;
			
			
	    long long ans = 0;
	    for (int i = 0; (K<<i) <= B; i++){
	    	long long l = (K<<i);
	    	long long r = (((K|1)+1)<<i)-1;
	    	l = max(l,A);
	    	r = min(r,B);
	    	ans += max(0LL, r-l+1);
	    }
	    return ans;
	}
};


