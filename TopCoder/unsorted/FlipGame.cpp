class FlipGame {
public:
	int minOperations(vector <string> a) {
		int n = a.size();
		int m = a[0].size();
		int ans = 0;
		while (true){
			int pos = n;
			for (int j = m-1; j >= 0; j--){
				for (int k = 0; k < pos; k++)
					if (a[k][j] == '1'){
						pos = k;
						break;
					}
				for (int k = pos; k < n; k++)
					a[k][j] = '1' - a[k][j] + '0';
			}
			if (pos == n)
				break;
			ans++;
		}
		return ans;
	}
};


