class TaroFriends {
public:
    int getNumber(vector <int> a, int x) {
        int best = (int)1e9;
        int n = a.size();
        for (int i = 0; i < n; i++){
        	for (int step = -1; step < 2; step += 2){
        		int lft = a[i] + x * step;
        		bool ok = true;
        		int rgh = -1e9;
        		for (int j = 0; j < n; j++) {
        			if (a[j] + x < lft) ok = false;
        			if (a[j] - x >= lft) rgh = max(rgh, a[j] - x);
        			else rgh = max(rgh, a[j] + x);
        		}
//        		eprintf("%d %d %d %d\n", i, step, rgh, lft);
        		if (ok) best = min(best, max(0,rgh - lft));
        	}
        }
        return best;
        
    }
};


