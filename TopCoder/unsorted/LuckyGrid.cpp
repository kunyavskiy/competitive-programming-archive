class Flow {
	struct Edge {
		int c, to, next;
		Edge(int c, int to, int next) :c(c), to(to), next(next){}
	};
	
	vector<Edge> es;
	vector<int> fir;
	int n;
	
	public:
	
	Flow(int _n):es(), fir(_n, -1), n(_n){}
	
	void addEdge(int a, int b, int c){
		es.pb(Edge(c, b, fir[a]));
		fir[a] = (int)es.size()-1;
		es.pb(Edge(0, a, fir[b]));
		fir[b] = (int)es.size()-1;
		assert(c >= 0);
		assert(0 <= a && a < n);
		assert(0 <= b && b < n);
	}
	
	vector<bool> used;
	
	int dfs(int v, int to, int minv, int cur){
		if (v == to) return cur;
		if (used[v]) return 0;
		used[v] = true;
		for (int i = fir[v]; i != -1; i = es[i].next){
			if (!es[i].c) continue;
			if (es[i].c < minv) continue;
			int temp = dfs(es[i].to, to, minv, min(cur, es[i].c));
			if (temp > 0) {
				es[i].c -= temp;
			    es[i^1].c += temp;
			    return temp;
			}
		}		    
		return 0;
	}
	
	int flow(int s, int e){
	    int tot = 0;
	    for (int i = 128; i > 0; i /= 2){
			while (true) {
		   		used = vector<bool>(n);
				int cur = dfs(s, e, i, 1e9);
				tot += cur;
				if (!cur) break;
			}
		}
		return tot;
	}
};

class LRFlow {
	vector<int> a;
	vector<int> b;
	vector<int> minc;
	vector<int> maxc;
	int n;
public:	
	LRFlow(int n) : n(n) {}
	
	void addEdge(int a, int b, int minc, int maxc) {
		this->a.pb(a);
		this->b.pb(b);
		this->minc.pb(minc);
		this->maxc.pb(maxc);
	}
	
	bool check(int s, int t, int m) {
		Flow f(n + 2);
		f.addEdge(t, s, m);
		int need = 0;
		for (int i = 0; i < (int)a.size(); i++) {
			if (minc[i] != 0) {
				f.addEdge(a[i], n + 1, minc[i]);
				f.addEdge(n, b[i], minc[i]);
				need += minc[i];
			}
			if (maxc[i] != minc[i]) {
				f.addEdge(a[i], b[i], maxc[i] - minc[i]);
			}
		}
		return f.flow(n, n+1) == need;
	}
	
	int flow(int s, int t) {
		int l = -1;
		int r = 10000;
		if (!check(s, t, r))
			return -1;
		while (r - l > 1) {
			int m = (l + r) / 2;
			if (!check(s, t, m)) {
				l = m;
			} else {
				r = m;
			}
		}
		return r;		
	}
	
	void dump(){
		eprintf("n = %d\n", n);
		for (int i = 0; i < (int)a.size(); i++)
			eprintf("%d %d %d %d\n", a[i], b[i], minc[i], maxc[i]);
	}
};


