class BuildingHeights {
public:
    int minimum(vector <int> v) {
        sort(v.begin(), v.end());
        int n = v.size();
        
        vector<int> sums(n+1);
        
        for (int i = 0; i < n; i++)
        	sums[i+1] = sums[i] + v[i];
		int ans = 0;        	
        for (int i = 1; i <= n; i++){
        	int cans = 1<<30;
        	for (int j = i; j <= n; j++)
        		cans = min(cans, v[j-1] * i - (sums[j] - sums[j-i]));
        	ans ^= cans;
        }
        return ans;
    }
};


