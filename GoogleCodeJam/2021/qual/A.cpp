void solve() {
  int n;
  scanf("%d", &n);
  vector<int> v(n);
  for (int &x : v) scanf("%d", &x);
  int ans = 0;
  for (int i = 0; i + 1 < n; i++) {
    int id = min_element(v.begin() + i, v.end()) - v.begin() + 1;
    ans += id - i;
    reverse(v.begin() + i, v.begin() + id);
  }
  printf("%d\n", ans);
}


