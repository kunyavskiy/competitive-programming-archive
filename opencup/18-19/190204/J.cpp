bool is_prime(int k) {
  for (int i = 2; i * i <= k; i++) {
    if (k % i == 0) return false;
  }
  return true;
}

void check() {

  int r1, r2, r3;
  r1 = r2 = r3 = 0;
  for (int k = 1; k <= 100; k++) {
    bool expect = false;
    if (k % 4 == 3) {
      expect = is_prime(k);
      r1 += expect;
    }
    if (k % 4 == 0) {
      int deg2 = 0;
      int kk = k;
      while (kk % 2 == 0) {
        kk /= 2;
        deg2++;
      }
      if (deg2 == 2 || deg2 == 4) {
        expect = is_prime(kk);
      }
      if (deg2 == 2) {
        r2 += expect;
      } else {
        r3 += expect;
      }
    }

    int cnt = 0;

    for (int b = 1; b <= 3 * k; b++) {
      for (int d = 1; d < b; d++) {
        int a = b - d;
        int cc = a * a + b * b + k;
        int x = sqrt(cc);
        if (x * x == cc && x == b + d) {
          cnt++;
        }
      }
    }
    bool real = cnt == 1;
    if (expect != real) {
      eprintf("fail %d, expect = %d, real = %d\n", k, expect, real);
      assert(0);
    } else {
      if (expect) {
        eprintf("ok %d, expect = %d, real = %d\n", k, expect, real);
      }
    }
  }
  eprintf("%d %d %d\n", r1, r2, r3);
}

const int MAGIC = 1e7;
const ll MAXN = 1e11 + 10;
const int SQ = sqrt(MAXN) + 10;

vector<int> primes;
bool ok[MAGIC];

void prepare() {
  memset(ok, true, SQ);
  for (int i = 2; i < SQ; i++) {
    if (ok[i]) {
      primes.push_back(i);
      for (int j = 2 * i; j < SQ; j += i) {
        ok[j] = false;
      }
    }
  }
}

pair<int, int> process_block(ll offset, int limit) {
  memset(ok, true, limit);
  if (offset == 0) {
    ok[0] = ok[1] = false;
  }
  for (int p : primes) {
    int shift = (int)(offset % p);
    if (shift) shift = p - shift;
    if (!offset) shift = 2 * p;
    for (ll j = shift; j < limit; j += p) {
      ok[j] = false;
    }
  }
  pair<int, int> res;
  for (int i = 0; i < limit; i++) {
    if (ok[i]) {
      res.first += 1;
      if ((offset + i) % 4 == 3) {
        res.second += 1;
      }
    }
  }
  return res;
}

void gen() {
  for (ll i = 0; i < MAXN; i += MAGIC) {
    TIMESTAMPf("%lld\n", i / MAGIC);
    auto r = process_block(i, MAGIC);
    printf("{%d, %d},\n", r.first, r.second);
  }
}

ll get_primes(ll n) {
  if (n == 0) return 0;
  if (n == 1) return 1;
  ll ans = 0;
  ll offset = 0;
  for (offset = 0; offset + MAGIC <= n; offset += MAGIC) {
    ans += data[offset / MAGIC].first;
  }
  auto r = process_block(offset, n - offset + 1);
  return ans + r.first;
}

ll get_primes_3(ll n) {
  ll ans = 0;
  ll offset = 0;
  for (offset = 0; offset + MAGIC <= n; offset += MAGIC) {
    ans += data[offset / MAGIC].second;
  }
  auto r = process_block(offset, n - offset + 1);
  return ans + r.second;
}


