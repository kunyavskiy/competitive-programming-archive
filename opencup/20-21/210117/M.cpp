typedef long long ll;
typedef long double ld;

const ll MOD = 1'000'000'000'000'000'031LL;

// mod should be not greater than 4e18
inline ll mul(ll a, ll b) {
  ll k = (ll)((long double)a * b / MOD);
  ll res = a * b - k * MOD;
  res %= MOD;
  if (res < 0) res += MOD;
  return res;
}

ll mpow(ll a, ll b) {
  ll res = 1;
  while (b) {
    if (b & 1)
      res = mul(res, a);
    a = mul(a, a);
    b >>= 1;
  }
  return res;
}

