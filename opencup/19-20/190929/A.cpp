  int n, m;
  while (scanf("%d%d", &n, &m) == 2) {
    int r, c;
    scanf("%*d%d%d", &r, &c);
    --r, --c;
    map<int, int> data;
    for (int i = 0; i < n; i++) {
      for (int j = 0; j < m; j++) {
        int a;
        scanf("%d", &a);
        if (a != 0) {
          data[a + abs(i - r) + abs(j - c)]++;
        }
      }
    }
    printf("%d\n", (int)data.size());
    for (auto x : data) {
      printf("%d %d\n", x.first, x.second);
    }
  }

  return 0;
